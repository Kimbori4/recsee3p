package com.furence.recsee.common.model;

public class LogoVO {
	private String logoType;
	private String logoChangeUse;
	private String logoPath;
	private String logoFullPath;
	private String logoSize;
	
	public String getLogoType() {
		return logoType;
	}
	public void setLogoType(String logoType) {
		this.logoType = logoType;
	}
	public String getLogoChangeUse() {
		return logoChangeUse;
	}
	public void setLogoChangeUse(String logoChangeUse) {
		this.logoChangeUse = logoChangeUse;
	}
	public String getLogoPath() {
		return logoPath;
	}
	public void setLogoPath(String logoPath) {
		this.logoPath = logoPath;
	}
	public String getLogoFullPath() {
		return logoFullPath;
	}
	public void setLogoFullPath(String logoFullPath) {
		this.logoFullPath = logoFullPath;
	}
	public String getLogoSize() {
		return logoSize;
	}
	public void setLogoSize(String logoSize) {
		this.logoSize = logoSize;
	}	
	
	
}
