package com.furence.recsee.monitoring.model;

public class SystemRealtimeInfo {
	private String rSysGroupCode;
	private String rSysIp;
	private String rSysName;
	private String rUseYn;
	
	public String getrSysGroupCode() {
		return rSysGroupCode;
	}
	public void setrSysGroupCode(String rSysGroupCode) {
		this.rSysGroupCode = rSysGroupCode;
	}
	public String getrSysIp() {
		return rSysIp;
	}
	public void setrSysIp(String rSysIp) {
		this.rSysIp = rSysIp;
	}
	public String getrSysName() {
		return rSysName;
	}
	public void setrSysName(String rSysName) {
		this.rSysName = rSysName;
	}
	public String getrUseYn() {
		return rUseYn;
	}
	public void setrUseYn(String rUseYn) {
		this.rUseYn = rUseYn;
	}
}
